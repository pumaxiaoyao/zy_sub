    /**
     * 组织生成订单信息
     *
     * @param $expect_orders
     * @param      $bet
     * @param null $panTable
     * @param      $user_id
     *
     * @return mixed
     * @throws \think\db\exception\BindParamException
     * @throws \think\db\exception\DataNotFoundException
     * @throws \think\db\exception\ModelNotFoundException
     * @throws \think\exception\DbException
     * @throws \think\exception\PDOException
     */
    private function generateOrderData(&$expect_orders, $bet, $panTable = null, $user_id = null)
    {
        if (is_null($user_id)) {
            $user = $this->user;
        } else {
            $user = AccUsers::get($user_id);
        }
        $order['username'] = $user->username;
        $order['nickname'] = $user->nickname;
        $order['tokenint'] = $user->tokenint;
        $order['money']    = $bet['money'];

        // 检查用户注额设置
        $userTrad = Pk10User::getByUser($user->tokenint);
        if ($bet['money'] < $userTrad->money_min) {
            return [
                'status' => 0,
                'msg'    => '下注失败，单注下注额不足最小下注限额'
            ];
        }
        if ($bet['money'] > $userTrad->money_max) {
            return [
                'status' => 0,
                'msg'    => '下注失败，单注下注额超出最大下注限额'
            ];
        }

        $order['order_no'] = order_no();
        usleep(mt_rand(1, 10));
        $order['lty_name'] = '北京PK拾';

        // 解析玩法和下注内容
        $content         = $this->handleKey(trim($bet['content']));
        $type            = $content['type'];
        $column          = $content['column'];
        $mark_a          = \config('pk10_type')[$type];
        $order['mark_a'] = $mark_a;
        $mark_b          = $this->getBetContent($type, $column);
        if (!$mark_b) {
            return [
                'status' => 0,
                'msg'    => '下注失败，参数错误'
            ];
        }
        $order['mark_b'] = $mark_b;

        // 赔率
        $odds_id = $this->getOddsId($type);
        if ($odds_id === 0) {
            return [
                'status' => 0,
                'msg'    => '下注失败！'
            ];
        }
        if (is_null($panTable)) {
            $pan      = Pk10Ratelist::getPanByUser($user->tokenint);
            $panTable = 'pk10_' . strtolower($pan);
        }

        // 检查用户该赔率表是否有定制赔率
        $customExists = Pk10CustomOdds::isExists($panTable, $user->tokenint);
        if ($customExists) {
            $odds = Pk10CustomOdds::getOddsByTableAndUser($panTable, $user->tokenint);
        } else {
            $odds = Pk10Odds::getAll($panTable)->toArray();
        }

        $result = compare_bet_money(Pk10Order::class, $odds, $bet, $order, $odds_id, $mark_b, $expect_orders);
        if (true !== $result) {
            return $result;
        }

        $rate = $odds[$odds_id - 1][$column];

        // 判断赔率是否跌倍
        $dec_odds = $odds[$odds_id - 1]['dec_odds'];
        if ($bet['money'] > $dec_odds[1]['limit']) {
            // 第二级跌倍
            $rate -= $dec_odds[1]['dec_odds'];
        } elseif ($bet['money'] > $dec_odds[0]['limit']) {
            // 第一级跌倍
            $rate -= $dec_odds[0]['dec_odds'];
        }
        // 默认不跌倍

        //        $rate = Pk10Odds::getRate($panTable, $odds_id, $column);
        $order['rate'] = $rate;
        $win           = $rate * $bet['money'];
        if ($win > $userTrad->money_win) {
            return [
                'status' => 0,
                'msg'    => '下注失败！预赢金额超标'
            ];
        }
        $order['win'] = $win;

        // 各级返点和返水

        $fs_g           = $odds[0]['A'];
        $fs_m           = $odds[1]['A'];
        $fs_a           = $odds[2]['A'];
        $fs_s           = $odds[3]['A'];
        $order['fs_g']  = $fs_g;
        $order['fs_m']  = $fs_m;
        $order['fs_a']  = $fs_a;
        $order['fs_s']  = $fs_s;
        $fs_gv          = $fs_g * $order['money'];
        $fs_mv          = $fs_m * $order['money'];
        $fs_av          = $fs_a * $order['money'];
        $fs_sv          = $fs_s * $order['money'];
        $order['fs_gv'] = $fs_gv;
        $order['fs_mv'] = $fs_mv;
        $order['fs_av'] = $fs_av;
        $order['fs_sv'] = $fs_sv;

        // 各级流水

        $ls_g           = $odds[0]['B'];
        $ls_m           = $odds[1]['B'];
        $ls_a           = $odds[2]['B'];
        $ls_s           = $odds[3]['B'];
        $order['ls_g']  = $ls_g;
        $order['ls_m']  = $ls_m;
        $order['ls_a']  = $ls_a;
        $order['ls_s']  = $ls_s;
        $ls_gv          = $ls_g * $order['money'];
        $ls_mv          = $ls_m * $order['money'];
        $ls_av          = $ls_a * $order['money'];
        $ls_sv          = $ls_s * $order['money'];
        $order['ls_gv'] = $ls_gv;
        $order['ls_mv'] = $ls_mv;
        $order['ls_av'] = $ls_av;
        $order['ls_sv'] = $ls_sv;

        // 开奖号码
        $order['open_code'] = '';
        // 输赢额
        $order['open_win'] = 0;
        // 是否结算
        $order['open_stu'] = 0;
        // 是否中奖
        $order['open_ret'] = 0;

        // 判断是否满足转盘条件，满足则标记为转盘订单
        $ifTrad = $bet['money'] > $userTrad->trad_max || $win > $userTrad->trad_win;
        if ($ifTrad && $userTrad->trad_url) {
            $order['trad_stu'] = 1;
            $order['trad_val'] = $bet['money'] * $userTrad->trad_rate;
            ;
            $order['trad_url']      = $userTrad->trad_url;
            $order['trad_tokensup'] = $userTrad->trad_tokensup;
            $order['trad_return']   = '';
        } else {
            $order['trad_stu']      = 0;
            $order['trad_val']      = 0;
            $order['trad_url']      = '';
            $order['trad_tokensup'] = '';
            $order['trad_return']   = '';
        }

        // 删除一些不需要的字段
        unset($order['content']);

        return $order;
    }

