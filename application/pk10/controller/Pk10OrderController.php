    /**
     * 拆分字段为玩法和下注内容
     *
     * @param $content
     *
     * @return array
     */
    private function handleKey($content)
    {
        $res         = explode('__', $content);
        $type        = $res[0];
        $bet_content = $res[1];
        return [
            'type'   => $type,
            'column' => $bet_content
        ];
    }

